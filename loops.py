# Loops in Python
# Loops means to repeat the same block of code multiple times.
# There are two types of loops in Python:
# 1. for loop
# 2. while loop

# for loopJ
# syntax:

# for variable in sequence:
#     code block
#     code block

# sequence can be a list, tuple, dictionary, set, or string.
#  when sequence is a string using range function is more common.

# Example:
# range(5) will generate a sequence of numbers from 0 to 4.
# for i in range(6):
#       print(i)

# while loop
# syntax:
# while condition:
#     code block
#     code block

# Example:
i = 5
while i < 6:
    print("Hii")
    i += 1 # i = i + 1



